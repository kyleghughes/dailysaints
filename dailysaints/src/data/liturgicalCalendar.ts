const entries = Object.entries(colourCalendar) as [
  LiturgicalColour,
  readonly string[],
][];

export const calendar: Record<string, LiturgicalColour> = Object.fromEntries(
  entries.flatMap(([colour, dates]) => dates.map((date) => [date, colour])),
);

/**
 * Returns the liturgical colour for a given calendar date.
 *
 * Looks up the colour using the provided month and day in the calendar mapping.
 * If no specific colour is defined for the date, `"white"` is returned as the default.
 *
 * @param month - The month of the year (1–12).
 * @param day - The day of the month.
 * @returns The liturgical colour for the specified date.
 */
export const getLiturgicalColour = (
  month: number,
  day: number,
): LiturgicalColour => {
  const key = `${month}-${day}`;

  return calendar[key] ?? "white"; // default
};
